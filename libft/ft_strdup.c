#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*str;
	int		i;
	int		cnt;

	i = 0;
	cnt = 0;
	while (s[cnt])
		cnt++;
	if (!(str = (char*)malloc(sizeof(char) * (cnt + 1))))
		return (NULL);
	while (s[i])
	{
		str[i] = s[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
